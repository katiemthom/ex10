#!/bin/env python

"""
Write the following two functions
    c_to_f(float) -> float
    f_to_c(float) -> float

c_to_f should convert a temperature 
in celsius to fahrenheit, and f_to_c 
should do the opposite
"""

def c_to_f(temp_in_c): 
	return temp_in_c * 33.8

def f_to_c(temp_in_f): 
	return temp_in_f/33.8